// Operadores de Comparação
void main() {
  int d = 10;
  print('d= $d');

  int e = 5;
  print('e= $e');

  bool maior = d > e;
  print('maior = d > e -> $maior ');

  bool menor = d < e;
  print('menor = d < e -> $menor');

  bool maiorOuIgual = d >= e;
  print('maiorouIgual = d >= e -> $maiorOuIgual');

  bool menorOuIgual = d <= e;
  print('menorOuIgual = d <= e -> $menorOuIgual');

  bool igual = d == e;
  print('igual = d = e -> $igual');

  bool diferente = d != e;
  print('diferente = d != e -> $diferente');
}
